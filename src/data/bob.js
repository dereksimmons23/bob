  // Check if an item is bob-adjacent
  isBobAdjacent(item) {
    if (!item) return false
    const lower = item.toLowerCase()
    return this.bobAdjacent.patterns.some(pattern => lower.includes(pattern))
  },

  // Get random line
  random(arr) {
    return arr[Math.floor(Math.random() * arr.length)]
  },

  // Get category-aware comment
  getCategoryComment(categoryType, commentType) {
    const comments = this[commentType]?.[categoryType] || this[commentType]?.random || this[commentType]
    return Array.isArray(comments) ? this.random(comments) : comments
  },
